export function translateBusinessDnaValue(value: string, locale: "en" | "ar"): string {
  if (locale === "en") return value;
  if (VALUE_EN_TO_AR[value]) return VALUE_EN_TO_AR[value];
  // Already stored in Arabic from Arabic quiz
  if (/[\u0600-\u06FF]/.test(value)) return value;
  return value;
}

export function translateIntelligenceTag(tag: string, locale: "en" | "ar"): string {
  if (locale === "en") return tag.replace(/_/g, " ");
  return TAG_EN_TO_AR[tag] ?? tag.replace(/_/g, " ");
}

